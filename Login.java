    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {                                         
    String username = txtUsername.getText();
    String password = txtPassword.getText();
    boolean loginSuccessful = false;
    String uname = null;
    String role = null; //to use role outside of the loop
 
    try (Scanner reader = new Scanner(new FileReader("C:\\Users\\User\\Desktop\\Car Rental\\account.txt"))) {
        reader.useDelimiter(",|\n"); //delimiter to split by comma or new line
        
        while (reader.hasNext()) {
            uname = reader.next().trim();
            String pw = reader.next().trim();
            String name = reader.next().trim(); //can use pattern?
            role = reader.next().trim();
            if (username.equals(uname) && password.equals(pw)) {
                loginSuccessful = true;            
                break;            
            }
        }
    } catch (Exception e) {
        //error if cannot find file
        JOptionPane.showMessageDialog(null, "Error reading from file: " + e.getMessage());
        return;
    }
    
    if (loginSuccessful) {        
        SessionManager.getInstance().setUser(uname, role);
        JOptionPane.showMessageDialog(null, "Login Successful as "+uname+" " + role);
        //next page
        
    }
    else {
        JOptionPane.showMessageDialog(null, "Invalid username or password");
    } 

    }                                        

