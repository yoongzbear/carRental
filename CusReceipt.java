    private void getDetail(String index) {
        //retireve detail from booking.txt and car info txt   
        System.out.println(index);
        Map<String, String> carInfoMap = new HashMap<>();

        //get car info
        try (BufferedReader reader = new BufferedReader(new FileReader("car_info.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length > 7) {                    
                    carInfoMap.put(parts[0].trim(), parts[1].trim()); 
                }
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Error reading car info file: " + e.getMessage());
        }
        
        //get booking info
        try (BufferedReader reader = new BufferedReader(new FileReader("cus_book_car.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length > 7) { 
                    String fileIndex = parts[0].trim();
                    if (fileIndex.equals(this.index)) { 
                        this.email = parts[1].trim();
                        this.carID = parts[2].trim();
                        this.totalRent = Double.parseDouble(parts[5].trim());
                        this.rentDate = parts[6].trim();  
                        this.returnDate = parts[7].trim();  
                        break; 
                    }
                }
            }
        } catch (FileNotFoundException e) {
            //if file not found
            JOptionPane.showMessageDialog(null, "File not found: " + e.getMessage());    
        } catch (Exception e) {
            //unexpected errors
            JOptionPane.showMessageDialog(null, "Error reading from file: " + e.getMessage());
        }
        
        //get customer info
        try (BufferedReader reader = new BufferedReader(new FileReader("account.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length > 5) { 
                    String fileEmail = parts[1].trim();
                    if (fileEmail.equals(this.email)) { 
                        this.name = parts[0].trim();
                        break; 
                    }
                }
            }
        } catch (FileNotFoundException e) {
            //if file not found
            JOptionPane.showMessageDialog(null, "File not found: " + e.getMessage());    
        } catch (Exception e) {
            //unexpected errors
            JOptionPane.showMessageDialog(null, "Error reading from file: " + e.getMessage());
        }
        //set car info
        this.carModel = carInfoMap.getOrDefault(this.carID, "Unknown");
        this.balance = this.amountPaid - this.totalRent;
    }
    
    private void printDetail(String index) {
        getDetail(index);
        emailLabel.setText(this.email);
        nameLabel.setText(this.name);
        bookingLabel.setText(this.index);
        modelLabel.setText(this.carModel);
        rentLabel.setText(this.rentDate);
        returnLabel.setText(this.returnDate);
        feeLabel.setText(Double.toString(this.totalRent));
        amountPaidLabel.setText(Double.toString(this.amountPaid));
        balanceLabel.setText(Double.toString(this.balance));
        instructionLabel.setText("Please proceed to car with Plate Number "+this.carID);
    }
    
